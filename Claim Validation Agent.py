def post_form(
    *,
    url: str,
    form: dict[str, Any],
    headers: dict[str, str] | None = None,
    timeout_seconds: int = 30,
) -> dict[str, Any]:
    request = urllib.request.Request(
        url,
        data=urllib.parse.urlencode(form).encode("utf-8"),
        headers={
            "Accept": "application/json",
            "Content-Type": "application/x-www-form-urlencoded",
            **(headers or {}),
        },
        method="POST",
    )
    try:
        with urllib.request.urlopen(request, timeout=timeout_seconds) as response:
            return json.loads(response.read().decode("utf-8"))
    except urllib.error.HTTPError as exc:
        body = exc.read().decode("utf-8", errors="replace")
        raise FHIRAuthError(f"FHIR token request failed with HTTP {exc.code}: {body}") from exc


def build_private_key_jwt_assertion(
    *,
    token_url: str,
    client_id: str,
    private_key_path: str,
    key_id: str,
    jwks_url: str | None = None,
    audience: str | None = None,
) -> str:
    from cryptography.hazmat.primitives import hashes, serialization
    from cryptography.hazmat.primitives.asymmetric import padding

    now = int(time.time())
    header = {
        "alg": "RS384",
        "typ": "JWT",
        "kid": key_id,
    }
    if jwks_url:
        header["jku"] = jwks_url
    payload = {
        "iss": client_id,
        "sub": client_id,
        "aud": audience or token_url,
        "jti": uuid4().hex,
        "nbf": now,
        "iat": now,
        "exp": now + 300,
    }

    signing_input = ".".join(
        [
            base64url(json.dumps(header, separators=(",", ":")).encode("utf-8")),
            base64url(json.dumps(payload, separators=(",", ":")).encode("utf-8")),
        ]
    ).encode("ascii")

    private_key = serialization.load_pem_private_key(
        resolve_local_path(private_key_path).read_bytes(),
        password=None,
    )
    signature = private_key.sign(
        signing_input,
        padding.PKCS1v15(),
        hashes.SHA384(),
    )
    return f"{signing_input.decode('ascii')}.{base64url(signature)}"


class FHIRTokenManager:
    def __init__(self) -> None:
        self._access_token: str | None = None
        self._expires_at_epoch: float = 0.0

    def invalidate(self) -> None:
        self._access_token = None
        self._expires_at_epoch = 0.0

    def access_token(self) -> str | None:
        static_token = os.getenv("FHIR_ACCESS_TOKEN")
        if static_token:
            return static_token

        auth_type = (os.getenv("FHIR_AUTH_TYPE") or DEFAULT_FHIR_AUTH_TYPE).strip().lower()
        if auth_type in {"", "none", "no_auth"}:
            return None
        if auth_type not in {
            "client_credentials",
            "backend",
            "backend_services",
            "backend_services_jwt",
            "private_key_jwt",
        }:
            return None

        if self._access_token and time.time() < self._expires_at_epoch - 60:
            return self._access_token

        token_response = self._fetch_backend_token(auth_type)
        access_token = token_response.get("access_token")
        if not access_token:
            raise FHIRAuthError("FHIR token response did not include access_token.")

        expires_in = int(float(token_response.get("expires_in") or 300))
        self._access_token = str(access_token)
        self._expires_at_epoch = time.time() + max(expires_in, 60)
        return self._access_token

    def _fetch_backend_token(self, auth_type: str) -> dict[str, Any]:
        token_url = os.getenv("FHIR_TOKEN_URL")
        client_id = os.getenv("FHIR_CLIENT_ID")
        client_secret = os.getenv("FHIR_CLIENT_SECRET")
        client_auth_method = (
            os.getenv("FHIR_CLIENT_AUTH_METHOD") or DEFAULT_FHIR_CLIENT_AUTH_METHOD
        ).strip().lower()
        scope = os.getenv("FHIR_SCOPE") or DEFAULT_FHIR_SCOPE
        audience = os.getenv("FHIR_AUDIENCE")
        private_key_path = os.getenv("FHIR_PRIVATE_KEY_PATH")
        key_id = os.getenv("FHIR_KEY_ID")
        jwks_url = os.getenv("FHIR_JWKS_URL")

        if auth_type in {"backend_services_jwt", "private_key_jwt"}:
            client_auth_method = "private_key_jwt"

        if not token_url:
            raise FHIRAuthError("FHIR backend auth requires FHIR_TOKEN_URL.")
        if not client_id:
            raise FHIRAuthError("FHIR backend auth requires FHIR_CLIENT_ID.")

        form: dict[str, Any] = {
            "grant_type": "client_credentials",
            "scope": scope,
        }
        headers: dict[str, str] = {}

        if client_auth_method == "client_secret_basic":
            if not client_secret:
                raise FHIRAuthError(
                    "FHIR_CLIENT_AUTH_METHOD=client_secret_basic requires FHIR_CLIENT_SECRET."
                )
            credentials = f"{client_id}:{client_secret}".encode("utf-8")
            headers["Authorization"] = f"Basic {b64encode(credentials).decode('ascii')}"
            if audience:
                form["aud"] = audience
        elif client_auth_method == "client_secret_post":
            if not client_secret:
                raise FHIRAuthError(
                    "FHIR_CLIENT_AUTH_METHOD=client_secret_post requires FHIR_CLIENT_SECRET."
                )
            form["client_id"] = client_id
            form["client_secret"] = client_secret
            if audience:
                form["aud"] = audience
        elif client_auth_method == "private_key_jwt":
            if not private_key_path:
                raise FHIRAuthError(
                    "FHIR_CLIENT_AUTH_METHOD=private_key_jwt requires FHIR_PRIVATE_KEY_PATH."
                )
            if not key_id:
                raise FHIRAuthError(
                    "FHIR_CLIENT_AUTH_METHOD=private_key_jwt requires FHIR_KEY_ID."
                )
            form["client_assertion_type"] = (
                "urn:ietf:params:oauth:client-assertion-type:jwt-bearer"
            )
            form["client_assertion"] = build_private_key_jwt_assertion(
                token_url=token_url,
                client_id=client_id,
                private_key_path=private_key_path,
                key_id=key_id,
                jwks_url=jwks_url,
                audience=audience or token_url,
            )
        else:
            raise FHIRAuthError(
                "Unsupported FHIR_CLIENT_AUTH_METHOD. Use client_secret_basic, "
                "client_secret_post, or private_key_jwt."
            )

        return post_form(url=token_url, form=form, headers=headers)


