def first_text(items: list[dict[str, Any]] | None) -> str | None:
    if not items:
        return None
    item = items[0]
    if item.get("text"):
        return item["text"]
    given = item.get("given", [])
    family = item.get("family")
    parts = [*given, family] if isinstance(given, list) else [given, family]
    return " ".join(str(part) for part in parts if part) or None


def first_codeable_text(codeable: dict[str, Any] | list[dict[str, Any]] | None) -> str | None:
    if isinstance(codeable, list):
        codeable = codeable[0] if codeable else None
    if not codeable:
        return None
    if isinstance(codeable, str):
        return codeable
    if codeable.get("text"):
        return codeable["text"]
    coding = codeable.get("coding", [])
    if coding:
        return coding[0].get("display") or coding[0].get("code")
    return None


def response_text_from_openai_chat(data: dict[str, Any]) -> str:
    choices = data.get("choices", [])
    if not choices:
        raise ValueError("Validation LLM response did not include choices.")
    message = choices[0].get("message", {})
    content = message.get("content")
    if not content:
        raise ValueError("Validation LLM response did not include message content.")
    return str(content)


def response_text_from_generate(data: dict[str, Any]) -> str:
    for key in ("generated_text", "text", "response", "output"):
        value = data.get(key)
        if isinstance(value, str):
            return value

    if isinstance(data.get("outputs"), list) and data["outputs"]:
        first = data["outputs"][0]
        if isinstance(first, str):
            return first
        if isinstance(first, dict):
            return str(first.get("text") or first.get("generated_text") or first)

    raise ValueError("Validation LLM generate response did not include text output.")


class ValidationRuleLLM:
    """Optional LLM adapter for narrative payer-rule interpretation."""

    def __init__(
        self,
        *,
        base_url: str | None = None,
        api_key: str | None = None,
        model: str | None = None,
        api_style: str | None = None,
        generate_path: str | None = None,
    ) -> None:
        self.base_url = (
            base_url
            or os.getenv("VALIDATION_LLM_BASE_URL")
            or os.getenv("MEDGEMMA_BASE_URL")
        )
        self.api_key = (
            api_key
            or os.getenv("VALIDATION_LLM_API_KEY")
            or os.getenv("MEDGEMMA_API_KEY")
        )
        self.model = (
            model
            or os.getenv("VALIDATION_LLM_MODEL")
            or os.getenv("MEDGEMMA_MODEL")
            or DEFAULT_VALIDATION_LLM_MODEL
        )
        self.api_style = (
            api_style
            or os.getenv("VALIDATION_LLM_API_STYLE")
            or os.getenv("MEDGEMMA_API_STYLE")
            or DEFAULT_VALIDATION_LLM_API_STYLE
        ).lower()
        self.generate_path = (
            generate_path
            or os.getenv("VALIDATION_LLM_GENERATE_PATH")
            or os.getenv("MEDGEMMA_GENERATE_PATH")
            or DEFAULT_VALIDATION_LLM_GENERATE_PATH
        )

    @property
    def is_configured(self) -> bool:
        return bool(self.base_url)

    def _prompt(self, *, payload: dict[str, Any]) -> str:
        instructions = """
You are a claim validation reviewer inside Velo Claim.

Evaluate only the provided payer rule against the claim facts and clinical text.
Do not invent diagnoses, procedures, payer rules, or external policy. If the
claim documentation satisfies the rule, return no issues. If it fails or needs
human review, return concise RCM-readable issues.

Return JSON only with this exact shape:
{
  "passes": true,
  "summary": "short explanation",
  "issues": [
    {
      "type": "LLM_RULE_REVIEW",
      "severity": "FAIL | WARN | INFO",
      "message": "string",
      "fix": "string or null",
      "evidence": "short quote or fact reference",
      "penalty": 8,
      "confidence": 0.0
    }
  ]
}
"""
        return (
            f"{instructions.strip()}\n\n"
            "INPUT:\n"
            f"{compact_json(payload)}"
        )

    def evaluate_rule(
        self,
        *,
        claim: ClaimInput,
        raw_claim: dict[str, Any],
        rule: dict[str, Any],
        fhir_context: dict[str, Any],
    ) -> dict[str, Any]:
        if not self.is_configured:
            raise RuntimeError("Validation LLM base URL is not configured.")

        payload = {
            "claim": claim.model_dump(),
            "raw_claim_excerpt": raw_claim,
            "payer_rule": rule,
            "fhir_context_keys": sorted(fhir_context.keys()),
            "fhir_context_excerpt": fhir_context,
        }
        prompt = self._prompt(payload=payload)

        if self.api_style == "openai_chat":
            data = post_json(
                base_url=self.base_url,
                path="/v1/chat/completions",
                bearer_token=self.api_key,
                timeout_seconds=90,
                payload={
                    "model": self.model,
                    "temperature": 0,
                    "max_tokens": 1400,
                    "messages": [
                        {
                            "role": "system",
                            "content": "You validate payer rules for Velo Claim.",
                        },
                        {"role": "user", "content": prompt},
                    ],
                },
            )
            return extract_json_object(response_text_from_openai_chat(data))

        if self.api_style == "generate":
            data = post_json(
                base_url=self.base_url,
                path=self.generate_path,
                bearer_token=self.api_key,
                timeout_seconds=90,
                payload={
                    "model": self.model,
                    "inputs": prompt,
                    "parameters": {"temperature": 0, "max_new_tokens": 1400},
                },
            )
            return extract_json_object(response_text_from_generate(data))

        raise ValueError(
            f"Unsupported VALIDATION_LLM_API_STYLE={self.api_style!r}. "
            "Use openai_chat or generate."
        )


def get_validation_llm(state: ClaimValidationState | None = None) -> ValidationRuleLLM | None:
    use_llm = bool(
        (state or {}).get("use_validation_llm", env_bool("USE_VALIDATION_LLM", False))
    )
    if not use_llm:
        return None

    client = ValidationRuleLLM()
    return client if client.is_configured else None


