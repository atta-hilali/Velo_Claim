def route_after_step(state: ClaimValidationState) -> str:
    return "error" if state.get("errors") else "continue"


NodeFunction = Callable[[ClaimValidationState], ClaimValidationState]


def safe_node(node_name: str, node_fn: NodeFunction) -> NodeFunction:
    def wrapped(state: ClaimValidationState) -> ClaimValidationState:
        if state.get("errors"):
            return with_event(
                state,
                node_name,
                "skipped",
                "Skipped because a previous validation error exists.",
            )

        try:
            result = node_fn(state)
            if result.get("errors"):
                return with_event(result, node_name, "failed", "Node returned validation errors.")
            return with_event(result, node_name, "completed")
        except (ValidationError, Exception) as exc:
            failed_state: ClaimValidationState = {
                **state,
                "status": "CLAIM_VALIDATION_FAILED",
                "errors": add_error(
                    state,
                    "VALIDATION_NODE_FAILED",
                    str(exc),
                    {"node": node_name},
                ),
            }
            return with_event(failed_state, node_name, "failed", str(exc))

    return wrapped


claim_validation_checkpointer = MemorySaver()


def build_claim_validation_graph(
    *,
    checkpointer: Any | None = None,
    interrupt_before_approval: bool = False,
):
    graph = StateGraph(ClaimValidationState)

    graph.add_node("normalize_input", safe_node("normalize_input", normalize_input_node))
    graph.add_node("fetch_fhir_context", safe_node("fetch_fhir_context", fetch_fhir_context_node))
    graph.add_node("load_rules", safe_node("load_rules", load_rules_node))
    graph.add_node("check_eligibility", safe_node("check_eligibility", check_eligibility_node))
    graph.add_node(
        "check_prior_authorization",
        safe_node("check_prior_authorization", check_prior_authorization_node),
    )
    graph.add_node(
        "check_coding_consistency",
        safe_node("check_coding_consistency", check_coding_consistency_node),
    )
    graph.add_node("check_documentation", safe_node("check_documentation", check_documentation_node))
    graph.add_node("apply_payer_rules", safe_node("apply_payer_rules", apply_payer_rules_node))
    graph.add_node("check_timely_filing", safe_node("check_timely_filing", check_timely_filing_node))
    graph.add_node("calculate_score", safe_node("calculate_score", calculate_score_node))
    graph.add_node("generate_report", safe_node("generate_report", generate_report_node))
    graph.add_node("approval_gate", safe_node("approval_gate", approval_gate_node))
    graph.add_node("error_handler", error_handler_node)

    graph.add_edge(START, "normalize_input")
    graph.add_conditional_edges(
        "normalize_input",
        route_after_step,
        {"continue": "fetch_fhir_context", "error": "error_handler"},
    )
    graph.add_conditional_edges(
        "fetch_fhir_context",
        route_after_step,
        {"continue": "load_rules", "error": "error_handler"},
    )
    graph.add_conditional_edges(
        "load_rules",
        route_after_step,
        {"continue": "check_eligibility", "error": "error_handler"},
    )
    graph.add_conditional_edges(
        "check_eligibility",
        route_after_step,
        {"continue": "check_prior_authorization", "error": "error_handler"},
    )
    graph.add_conditional_edges(
        "check_prior_authorization",
        route_after_step,
        {"continue": "check_coding_consistency", "error": "error_handler"},
    )
    graph.add_conditional_edges(
        "check_coding_consistency",
        route_after_step,
        {"continue": "check_documentation", "error": "error_handler"},
    )
    graph.add_conditional_edges(
        "check_documentation",
        route_after_step,
        {"continue": "apply_payer_rules", "error": "error_handler"},
    )
    graph.add_conditional_edges(
        "apply_payer_rules",
        route_after_step,
        {"continue": "check_timely_filing", "error": "error_handler"},
    )
    graph.add_conditional_edges(
        "check_timely_filing",
        route_after_step,
        {"continue": "calculate_score", "error": "error_handler"},
    )
    graph.add_conditional_edges(
        "calculate_score",
        route_after_step,
        {"continue": "generate_report", "error": "error_handler"},
    )
    graph.add_conditional_edges(
        "generate_report",
        route_after_step,
        {"continue": "approval_gate", "error": "error_handler"},
    )
    graph.add_conditional_edges(
        "approval_gate",
        route_after_step,
        {"continue": END, "error": "error_handler"},
    )
    graph.add_edge("error_handler", END)

    compile_kwargs: dict[str, Any] = {
        "checkpointer": checkpointer if checkpointer is not None else claim_validation_checkpointer
    }
    if interrupt_before_approval:
        compile_kwargs["interrupt_before"] = ["approval_gate"]

    return graph.compile(**compile_kwargs)


claim_validation_agent = build_claim_validation_graph()


def validation_thread_id(initial_state: ClaimValidationState | dict[str, Any]) -> str:
    raw_claim = claim_from_state(initial_state)
    claim_id = raw_claim.get("claim_id") or raw_claim.get("id") or "unknown-claim"
    return f"{DEFAULT_VALIDATION_THREAD_PREFIX}:{claim_id}:{uuid4().hex}"


def validation_graph_config(thread_id: str | None = None) -> dict[str, Any]:
    return {"configurable": {"thread_id": thread_id or f"{DEFAULT_VALIDATION_THREAD_PREFIX}:{uuid4().hex}"}}


def run_claim_validation(
    initial_state: ClaimValidationState | dict[str, Any],
    *,
    thread_id: str | None = None,
    interrupt_before_approval: bool = False,
) -> ClaimValidationState:
    state = dict(initial_state)
    effective_thread_id = thread_id or validation_thread_id(state)
    state["thread_id"] = effective_thread_id
    if interrupt_before_approval:
        state["interrupt_before_approval"] = True

    agent = (
        build_claim_validation_graph(interrupt_before_approval=True)
        if interrupt_before_approval
        else claim_validation_agent
    )
    config = validation_graph_config(effective_thread_id)
    return agent.invoke(state, config=config)


def resume_claim_validation_after_approval(
    *,
    thread_id: str,
    approval: dict[str, Any] | None = None,
) -> ClaimValidationState:
    state_update = {"approval": approval or {"decision": "approved", "status": "approved"}}
    return claim_validation_agent.invoke(state_update, config=validation_graph_config(thread_id))


