def kg_edges_for_procedure(kg: dict[str, Any], procedure_code: str) -> list[dict[str, Any]]:
    code = normalize_code(procedure_code)
    return [
        edge
        for edge in as_list(kg.get("edges"))
        if isinstance(edge, dict)
        and normalize_code(edge.get("procedure_code")) == code
    ]


def kg_payer_rule_issues(
    *,
    claim: ClaimInput,
    kg: dict[str, Any] | None,
) -> tuple[list[ValidationIssue], dict[str, Any]]:
    if not kg:
        return [], {"enabled": False, "reason": "knowledge graph not configured"}

    diagnosis_codes = {normalize_code(code) for code in claim.icd_codes if normalize_code(code)}
    procedure_codes = list(dict.fromkeys(claim_procedure_codes(claim)))
    if not diagnosis_codes or not procedure_codes:
        return [], {
            "enabled": True,
            "version": kg.get("version"),
            "checked_procedure_codes": procedure_codes,
            "skipped": "missing diagnosis or procedure codes",
        }

    issues: list[ValidationIssue] = []
    matched_edges: list[dict[str, Any]] = []
    unsupported_procedures: list[str] = []

    for procedure_code in procedure_codes:
        candidate_edges = kg_edges_for_procedure(kg, procedure_code)
        if not candidate_edges:
            continue

        supporting_edges = [
            edge
            for edge in candidate_edges
            if normalize_code(edge.get("diagnosis_code")) in diagnosis_codes
            and normalize_token(edge.get("relationship")) in {"supports", "medically_necessary_for", "maps_to"}
        ]
        contraindicated_edges = [
            edge
            for edge in candidate_edges
            if normalize_code(edge.get("diagnosis_code")) in diagnosis_codes
            and normalize_token(edge.get("relationship")) == "contraindicated"
        ]

        matched_edges.extend(supporting_edges)
        if contraindicated_edges:
            edge = contraindicated_edges[0]
            issues.append(
                ValidationIssue(
                    layer="PAYER_RULES",
                    type="KG_CODE_CONTRAINDICATED",
                    severity="FAIL",
                    message=(
                        f"KG marks procedure {procedure_code} as contraindicated "
                        f"for diagnosis {edge.get('diagnosis_code')}."
                    ),
                    fix="Route to coding review and correct the diagnosis/procedure pairing.",
                    evidence=edge.get("evidence") or edge.get("id"),
                    penalty=15,
                    confidence=float(edge.get("confidence") or 0.8),
                )
            )
            continue

        if not supporting_edges:
            supported_diagnoses = sorted(
                {
                    normalize_code(edge.get("diagnosis_code"))
                    for edge in candidate_edges
                    if edge.get("diagnosis_code")
                }
            )
            unsupported_procedures.append(procedure_code)
            issues.append(
                ValidationIssue(
                    layer="PAYER_RULES",
                    type="KG_ICD_PROCEDURE_MISMATCH",
                    severity="WARN",
                    message=(
                        f"KG has support edges for procedure {procedure_code}, "
                        "but none match the claim diagnosis codes."
                    ),
                    fix=(
                        "Confirm the documented diagnosis supports this procedure, "
                        "or correct the ICD/procedure code pairing."
                    ),
                    evidence=(
                        f"Claim ICDs={sorted(diagnosis_codes)}; "
                        f"KG-supported ICDs={supported_diagnoses}"
                    ),
                    penalty=8,
                    confidence=0.75,
                )
            )

    return issues, {
        "enabled": True,
        "version": kg.get("version"),
        "checked_procedure_codes": procedure_codes,
        "matched_edges": [
            {
                "id": edge.get("id"),
                "diagnosis_code": edge.get("diagnosis_code"),
                "procedure_code": edge.get("procedure_code"),
                "relationship": edge.get("relationship"),
                "confidence": edge.get("confidence"),
            }
            for edge in matched_edges
        ],
        "unsupported_procedure_codes": unsupported_procedures,
    }


def rule_requires_llm(rule: dict[str, Any]) -> bool:
    action = rule.get("action", {})
    return bool(rule.get("requires_llm") or action.get("requires_llm"))


def llm_issue_from_payload(item: dict[str, Any], rule: dict[str, Any]) -> ValidationIssue:
    severity = str(item.get("severity") or rule.get("severity") or "WARN").upper()
    if severity not in {"FAIL", "WARN", "INFO"}:
        severity = "WARN"
    default_penalty = 15 if severity == "FAIL" else 8 if severity == "WARN" else 0
    return ValidationIssue(
        layer="PAYER_RULES",
        type=normalize_code(item.get("type")) or "LLM_RULE_REVIEW",
        severity=severity,  # type: ignore[arg-type]
        message=str(item.get("message") or rule.get("message") or "LLM payer-rule review issue."),
        fix=item.get("fix") or rule.get("action", {}).get("fix_template"),
        evidence=item.get("evidence") or f"Rule {rule.get('rule_id')}",
        penalty=float(item.get("penalty") if item.get("penalty") is not None else default_penalty),
        confidence=float(item.get("confidence") if item.get("confidence") is not None else 0.75),
    )


def run_llm_payer_rule_checks(
    *,
    state: ClaimValidationState,
    claim: ClaimInput,
    rules: list[dict[str, Any]],
) -> tuple[list[ValidationIssue], dict[str, Any]]:
    llm_rules = [
        rule
        for rule in rules
        if rule_requires_llm(rule) and rule_condition_matches_claim(rule, claim)
    ]
    if not llm_rules:
        return [], {"enabled": False, "reason": "no matching requires_llm rules"}

    llm = get_validation_llm(state)
    if not llm:
        return [], {
            "enabled": False,
            "reason": "validation LLM not configured or disabled",
            "skipped_rules": [rule.get("rule_id") for rule in llm_rules],
        }

    issues: list[ValidationIssue] = []
    evaluations: list[dict[str, Any]] = []
    for rule in llm_rules:
        try:
            data = llm.evaluate_rule(
                claim=claim,
                raw_claim=state.get("raw_claim", {}),
                rule=rule,
                fhir_context=state.get("fhir_context", {}),
            )
            evaluations.append(
                {
                    "rule_id": rule.get("rule_id"),
                    "passes": data.get("passes"),
                    "summary": data.get("summary"),
                    "issue_count": len(as_list(data.get("issues"))),
                }
            )
            for item in as_list(data.get("issues")):
                if isinstance(item, dict):
                    issues.append(llm_issue_from_payload(item, rule))
        except Exception as exc:
            evaluations.append(
                {
                    "rule_id": rule.get("rule_id"),
                    "passes": None,
                    "error": str(exc),
                }
            )
            issues.append(
                ValidationIssue(
                    layer="PAYER_RULES",
                    type="LLM_RULE_CHECK_FAILED",
                    severity="WARN",
                    message=f"LLM payer-rule check failed for rule {rule.get('rule_id')}.",
                    fix="Run manual payer-rule review or retry after LLM service is available.",
                    evidence=str(exc),
                    penalty=0,
                    confidence=1.0,
                )
            )

    return issues, {
        "enabled": True,
        "rules_checked": [rule.get("rule_id") for rule in llm_rules],
        "evaluations": evaluations,
    }


VOI_FIELD_TOKENS = {
    "voi",
    "voiflag",
    "voistatus",
    "damanvoi",
    "damanvoiflag",
    "damanvoistatus",
    "verificationofinsurance",
    "verificationofinsuranceflag",
    "verificationofinsurancestatus",
}


def extension_value(extension: dict[str, Any]) -> Any:
    for key in (
        "valueBoolean",
        "valueCode",
        "valueString",
        "valueInteger",
        "valueDecimal",
        "valueDate",
        "valueDateTime",
    ):
        if key in extension:
            return extension[key]
    if isinstance(extension.get("valueCodeableConcept"), dict):
        return first_codeable_text(extension["valueCodeableConcept"])
    return None


def find_voi_value(value: Any) -> Any:
    if isinstance(value, dict):
        for extension in as_list(value.get("extension")):
            if (
                isinstance(extension, dict)
                and "voi" in compact_token(extension.get("url"))
            ):
                extracted = extension_value(extension)
                if extracted is not None:
                    return extracted

        for key, nested_value in value.items():
            if compact_token(key) in VOI_FIELD_TOKENS:
                return nested_value
            found = find_voi_value(nested_value)
            if found is not None:
                return found

    if isinstance(value, list):
        for item in value:
            found = find_voi_value(item)
            if found is not None:
                return found

    return None


def voi_status_from_value(value: Any) -> dict[str, Any]:
    if value is None:
        return {"present": False, "verified": None, "status": None}
    token = compact_token(value)
    verified_tokens = {
        "true",
        "yes",
        "y",
        "1",
        "active",
        "approved",
        "valid",
        "verified",
        "voiapproved",
        "voiverified",
    }
    failed_tokens = {
        "false",
        "no",
        "n",
        "0",
        "inactive",
        "denied",
        "expired",
        "invalid",
        "missing",
        "notverified",
        "voimissing",
        "voiexpired",
    }
    verified: bool | None
    if token in verified_tokens:
        verified = True
    elif token in failed_tokens:
        verified = False
    else:
        verified = None
    return {
        "present": True,
        "verified": verified,
        "status": str(value),
    }


def payer_is_daman(claim: ClaimInput, coverage: dict[str, Any] | None) -> bool:
    if payer_matches("daman", claim.payer_id) or payer_matches("daman", claim.plan_id):
        return True
    for payor in as_list((coverage or {}).get("payor")):
        if isinstance(payor, dict):
            values = [
                payor.get("display"),
                payor.get("identifier", {}).get("value")
                if isinstance(payor.get("identifier"), dict)
                else None,
            ]
            if any(payer_matches("daman", value) for value in values if value):
                return True
    return False


def daman_voi_from_context(
    claim: ClaimInput,
    context: dict[str, Any],
    coverage: dict[str, Any] | None,
) -> dict[str, Any]:
    is_daman = payer_is_daman(claim, coverage)
    sources = [
        ("EligibilityResponse", context.get("EligibilityResponse")),
        ("Coverage", coverage),
    ]
    for source_name, source in sources:
        value = find_voi_value(source)
        if value is not None:
            return {
                **voi_status_from_value(value),
                "payer_is_daman": is_daman,
                "source": source_name,
            }
    return {
        "present": False,
        "verified": None,
        "status": None,
        "payer_is_daman": is_daman,
        "source": None,
    }


def check_eligibility_node(state: ClaimValidationState) -> ClaimValidationState:
    if state.get("errors"):
        return state

    claim = ClaimInput.model_validate(state["claim"])
    context = state.get("fhir_context", {})
    coverage = context.get("Coverage")
    issues: list[ValidationIssue] = []
    daman_voi = daman_voi_from_context(claim, context, coverage)

    if not coverage:
        issues.append(
            ValidationIssue(
                layer="ELIGIBILITY",
                type="MISSING_COVERAGE",
                severity="FAIL",
                message="No coverage was found for the patient.",
                fix="Verify patient insurance details or request updated coverage information.",
                penalty=20,
            )
        )
    else:
        status = normalize_token(coverage.get("status"))
        if status != "active":
            issues.append(
                ValidationIssue(
                    layer="ELIGIBILITY",
                    type="INACTIVE_COVERAGE",
                    severity="FAIL",
                    message=f"Coverage status is {coverage.get('status')}, not active.",
                    fix="Do not submit until active coverage is confirmed.",
                    evidence=f"Coverage/{coverage.get('id')}",
                    penalty=20,
                )
            )
        period = coverage.get("period") or {}
        if period and not date_in_period(claim.date_of_service, period):
            issues.append(
                ValidationIssue(
                    layer="ELIGIBILITY",
                    type="COVERAGE_OUTSIDE_SERVICE_DATE",
                    severity="FAIL",
                    message="Coverage period does not include the date of service.",
                    fix="Confirm another active policy for the service date.",
                    evidence=json.dumps(period, ensure_ascii=True),
                    penalty=20,
                )
            )

        if daman_voi["payer_is_daman"] and not daman_voi["present"]:
            issues.append(
                ValidationIssue(
                    layer="ELIGIBILITY",
                    type="DAMAN_VOI_NOT_RETURNED",
                    severity="WARN",
                    message="DAMAN eligibility response did not include a VOI flag.",
                    fix="Run or retrieve DAMAN VOI/eligibility verification before PA or claim submission.",
                    evidence=f"Coverage/{coverage.get('id')}",
                    penalty=5,
                )
            )
        elif daman_voi["payer_is_daman"] and daman_voi["verified"] is False:
            issues.append(
                ValidationIssue(
                    layer="ELIGIBILITY",
                    type="DAMAN_VOI_NOT_VERIFIED",
                    severity="FAIL",
                    message="DAMAN VOI is present but not verified for this service date.",
                    fix="Resolve VOI with DAMAN before PA or claim submission.",
                    evidence=f"{daman_voi.get('source')} VOI={daman_voi.get('status')}",
                    penalty=20,
                )
            )

    result = CheckResult(
        layer="ELIGIBILITY",
        passes=not any(issue.severity == "FAIL" for issue in issues),
        issues=issues,
        raw_data={
            "coverage_id": coverage.get("id") if coverage else None,
            "plan_id": claim.plan_id,
            "daman_voi": daman_voi,
        },
    )
    return {**state, "eligibility_result": result_dict(result)}


def code_in_rule(rule: dict[str, Any], code: str) -> bool:
    return code_matches_condition_code(code, rule.get("condition", {})) or normalize_code(code) in {
        normalize_code(item)
        for item in [
            *as_list(rule.get("cpt_codes")),
            *as_list(rule.get("procedure_codes")),
        ]
        if normalize_code(item)
    }


def auth_covers_code(auth: dict[str, Any], code: str, service_date: str) -> bool:
    return evaluate_prior_auth_for_code(auth, code, service_date)["covers"]


def auth_code_matches(auth: dict[str, Any], code: str) -> bool:
    codes = {
        normalize_code(item)
        for item in as_list(auth.get("cpt_codes") or auth.get("procedure_codes"))
    }
    return not codes or normalize_code(code) in codes


def auth_validity_dates(auth: dict[str, Any]) -> tuple[str | None, str | None]:
    return (
        auth.get("valid_from") or auth.get("from") or auth.get("start"),
        auth.get("valid_to") or auth.get("to") or auth.get("expires") or auth.get("end"),
    )


def evaluate_prior_auth_for_code(
    auth: dict[str, Any],
    code: str,
    service_date: str,
) -> dict[str, Any]:
    if not auth:
        return {"covers": False, "reason": "missing"}
    status = normalize_token(auth.get("status"))
    approved = auth.get("approved") is True or status in {"approved", "active"}
    if not approved:
        return {"covers": False, "reason": "not_approved", "status": status}

    if not auth_code_matches(auth, code):
        return {"covers": False, "reason": "code_not_covered", "status": status}

    valid_from, valid_to = auth_validity_dates(auth)
    service = parse_iso_date(service_date)
    starts = parse_iso_date(valid_from)
    expires = parse_iso_date(valid_to)
    if service and starts and service < starts:
        return {
            "covers": False,
            "reason": "not_yet_valid",
            "status": status,
            "valid_from": valid_from,
            "valid_to": valid_to,
        }
    if service and expires and service > expires:
        return {
            "covers": False,
            "reason": "expired",
            "status": status,
            "valid_from": valid_from,
            "valid_to": valid_to,
        }

    return {
        "covers": True,
        "reason": "covered",
        "status": status,
        "valid_from": valid_from,
        "valid_to": valid_to,
    }


def check_prior_authorization_node(state: ClaimValidationState) -> ClaimValidationState:
    if state.get("errors"):
        return state

    claim = ClaimInput.model_validate(state["claim"])
    rules = state.get("applicable_rules", [])
    auth_records = as_list(state.get("fhir_context", {}).get("PriorAuthorization"))
    if claim.prior_auth:
        auth_records.append(claim.prior_auth)

    issues: list[ValidationIssue] = []
    auth_evaluations: list[dict[str, Any]] = []
    for code in dict.fromkeys(claim_procedure_codes(claim)):
        matching_rules = [
            rule
            for rule in rules
            if rule.get("rule_type") in {"PRIOR_AUTH", "PRIOR_AUTHORIZATION"}
            and rule_requires_auth_for_code(rule, claim, code)
        ]
        if not matching_rules:
            continue

        code_evaluations = [
            {
                **evaluate_prior_auth_for_code(auth, code, claim.date_of_service),
                "code": code,
                "auth_id": auth.get("id") or auth.get("authorization_id") or auth.get("ref"),
            }
            for auth in auth_records
            if isinstance(auth, dict)
        ]
        auth_evaluations.extend(code_evaluations)
        has_auth = any(evaluation.get("covers") for evaluation in code_evaluations)
        if not has_auth:
            rule = matching_rules[0]
            expired_auth = next(
                (
                    evaluation
                    for evaluation in code_evaluations
                    if evaluation.get("reason") == "expired"
                ),
                None,
            )
            not_yet_valid_auth = next(
                (
                    evaluation
                    for evaluation in code_evaluations
                    if evaluation.get("reason") == "not_yet_valid"
                ),
                None,
            )
            if expired_auth:
                issues.append(
                    ValidationIssue(
                        layer="PRIOR_AUTHORIZATION",
                        type="EXPIRED_PRIOR_AUTH",
                        severity=rule.get("severity", "FAIL"),
                        message=(
                            f"Prior authorization for procedure code {code} expired "
                            f"before the date of service."
                        ),
                        fix=f"Renew prior authorization for procedure code {code} before submission.",
                        evidence=(
                            f"Auth {expired_auth.get('auth_id') or 'record'} valid_to="
                            f"{expired_auth.get('valid_to')}; service_date={claim.date_of_service}"
                        ),
                        penalty=25 if rule.get("severity", "FAIL") == "FAIL" else 10,
                    )
                )
                continue
            if not_yet_valid_auth:
                issues.append(
                    ValidationIssue(
                        layer="PRIOR_AUTHORIZATION",
                        type="PRIOR_AUTH_NOT_YET_VALID",
                        severity=rule.get("severity", "FAIL"),
                        message=(
                            f"Prior authorization for procedure code {code} starts "
                            f"after the date of service."
                        ),
                        fix=f"Use an authorization valid on {claim.date_of_service}.",
                        evidence=(
                            f"Auth {not_yet_valid_auth.get('auth_id') or 'record'} valid_from="
                            f"{not_yet_valid_auth.get('valid_from')}; service_date={claim.date_of_service}"
                        ),
                        penalty=25 if rule.get("severity", "FAIL") == "FAIL" else 10,
                    )
                )
                continue
            issues.append(
                ValidationIssue(
                    layer="PRIOR_AUTHORIZATION",
                    type="MISSING_PRIOR_AUTH",
                    severity=rule.get("severity", "FAIL"),
                    message=rule.get("message") or f"Procedure code {code} requires prior authorization.",
                    fix=f"Obtain prior authorization for procedure code {code} before claim submission.",
                    evidence=f"Rule {rule.get('rule_id')}",
                    penalty=25 if rule.get("severity", "FAIL") == "FAIL" else 10,
                )
            )

    result = CheckResult(
        layer="PRIOR_AUTHORIZATION",
        passes=not any(issue.severity == "FAIL" for issue in issues),
        issues=issues,
        raw_data={"auth_records": auth_records, "auth_evaluations": auth_evaluations},
    )
    return {**state, "prior_auth_result": result_dict(result)}


def check_coding_consistency_node(state: ClaimValidationState) -> ClaimValidationState:
    if state.get("errors"):
        return state

    claim = ClaimInput.model_validate(state["claim"])
    note = (claim.soap_note_excerpt or "").lower()
    issues: list[ValidationIssue] = []

    if not claim.icd_codes:
        issues.append(
            ValidationIssue(
                layer="CODING",
                type="MISSING_CODE",
                severity="FAIL",
                message="No diagnosis code is present on the claim.",
                fix="Add at least one diagnosis code supported by the encounter documentation.",
                penalty=20,
            )
        )

    if not (claim.cpt_codes or claim.procedure_codes):
        issues.append(
            ValidationIssue(
                layer="CODING",
                type="MISSING_CODE",
                severity="FAIL",
                message="No procedure/service code is present on the claim.",
                fix="Add the procedure/service code from the encounter or charge line.",
                penalty=20,
            )
        )

    if not note:
        issues.append(
            ValidationIssue(
                layer="CODING",
                type="MISSING_DOCUMENTATION_CONTEXT",
                severity="WARN",
                message="No SOAP note excerpt is available for coding consistency review.",
                fix="Attach or extract the SOAP note before final validation.",
                penalty=8,
            )
        )

    if "99214" in claim.cpt_codes and note:
        complexity_terms = {"moderate", "complex", "multiple", "prescription", "exacerbation"}
        if not any(term in note for term in complexity_terms):
            issues.append(
                ValidationIssue(
                    layer="CODING",
                    type="UPCODING",
                    severity="WARN",
                    message="The documentation may not support CPT 99214 complexity.",
                    fix="Use a lower E/M code or add documentation supporting moderate complexity.",
                    evidence="CPT 99214",
                    penalty=10,
                    confidence=0.8,
                )
            )

    result = CheckResult(
        layer="CODING",
        passes=not any(issue.severity == "FAIL" for issue in issues),
        issues=issues,
    )
    return {**state, "coding_result": result_dict(result)}


def document_types_from_claim(claim: ClaimInput, context: dict[str, Any]) -> set[str]:
    document_types = {
        normalize_token(doc.get("type") or doc.get("category"))
        for doc in claim.attachments
        if isinstance(doc, dict)
    }
    for document in as_list(context.get("DocumentReference")):
        if isinstance(document, dict):
            doc_type = first_codeable_text(document.get("type")) or document.get("type")
            if doc_type:
                document_types.add(normalize_token(doc_type))
    return {item for item in document_types if item}


def check_documentation_node(state: ClaimValidationState) -> ClaimValidationState:
    if state.get("errors"):
        return state

    claim = ClaimInput.model_validate(state["claim"])
    context = state.get("fhir_context", {})
    rules = state.get("applicable_rules", [])
    document_types = document_types_from_claim(claim, context)
    issues: list[ValidationIssue] = []

    if not context.get("Encounter") and not claim.encounter_id:
        issues.append(
            ValidationIssue(
                layer="DOCUMENTATION",
                type="MISSING_ENCOUNTER",
                severity="FAIL",
                message="Encounter resource is missing.",
                fix="Fetch or attach the source encounter before submission.",
                penalty=15,
            )
        )

    if not claim.soap_note_excerpt and "soap_note" not in document_types and "soap note" not in document_types:
        issues.append(
            ValidationIssue(
                layer="DOCUMENTATION",
                type="MISSING_SOAP_NOTE",
                severity="FAIL",
                message="SOAP note documentation is missing.",
                fix="Attach or extract the SOAP note before submission.",
                penalty=15,
            )
        )

    if not claim.icd_codes:
        issues.append(
            ValidationIssue(
                layer="DOCUMENTATION",
                type="MISSING_DIAGNOSIS",
                severity="FAIL",
                message="No documented diagnosis is available.",
                fix="Add diagnosis evidence from the encounter or Condition resource.",
                penalty=15,
            )
        )

    if not (claim.cpt_codes or claim.procedure_codes):
        issues.append(
            ValidationIssue(
                layer="DOCUMENTATION",
                type="MISSING_PROCEDURE",
                severity="FAIL",
                message="No documented procedure/service is available.",
                fix="Add procedure evidence from the encounter, Procedure resource, or charge line.",
                penalty=15,
            )
        )

    required_docs: set[str] = set()
    for rule in rules:
        if rule.get("rule_type") == "DOCUMENTATION":
            if not rule_condition_matches_claim(rule, claim):
                continue
            required_docs.update(normalize_token(item) for item in as_list(rule.get("action", {}).get("required_documents")))
            required_docs.update(normalize_token(item) for item in as_list(rule.get("required_documents")))

    missing_docs = {doc for doc in required_docs if doc and doc not in document_types}
    for doc_type in sorted(missing_docs):
        issues.append(
            ValidationIssue(
                layer="DOCUMENTATION",
                type="MISSING_DOCUMENT",
                severity="FAIL",
                message=f"Required document is missing: {doc_type}.",
                fix=f"Attach {doc_type} before claim submission.",
                penalty=15,
            )
        )

    result = CheckResult(
        layer="DOCUMENTATION",
        passes=not any(issue.severity == "FAIL" for issue in issues),
        issues=issues,
        raw_data={"available_document_types": sorted(document_types)},
    )
    return {**state, "documentation_result": result_dict(result)}


def apply_payer_rules_node(state: ClaimValidationState) -> ClaimValidationState:
    if state.get("errors"):
        return state

    claim = ClaimInput.model_validate(state["claim"])
    rules = state.get("applicable_rules", [])
    issues: list[ValidationIssue] = []
    deterministic_rule_ids: list[str] = []

    for rule in rules:
        rule_type = rule.get("rule_type")
        condition = rule.get("condition", {})
        action = rule.get("action", {})
        severity = rule.get("severity", "FAIL")
        penalty = 15 if severity == "FAIL" else 8

        if rule_type == "CPT_REQUIRES_ICD":
            deterministic_rule_ids.append(rule.get("rule_id"))
            matching_codes = [
                code for code in claim.cpt_codes if code_matches_condition_code(code, condition)
            ]
            required_prefixes = [normalize_code(item) for item in as_list(action.get("required_icd_prefixes"))]
            for cpt in matching_codes:
                has_required_icd = any(
                    any(icd.startswith(prefix) for prefix in required_prefixes)
                    for icd in claim.icd_codes
                )
                if not has_required_icd:
                    issues.append(
                        ValidationIssue(
                            layer="PAYER_RULES",
                            type="CPT_REQUIRES_ICD",
                            severity=severity,
                            message=rule.get("message") or f"CPT {cpt} requires a matching ICD.",
                            fix=(
                                f"Add an ICD code matching one of {required_prefixes}, "
                                f"or remove CPT {cpt}."
                            ),
                            evidence=f"Rule {rule.get('rule_id')}",
                            penalty=penalty,
                        )
                    )

        if rule_type == "MAX_UNITS":
            deterministic_rule_ids.append(rule.get("rule_id"))
            cpt = normalize_code(condition.get("cpt"))
            max_units = int(action.get("max_units", 1))
            for line in claim.line_items:
                if normalize_code(line.get("code")) == cpt:
                    units = line_quantity(line)
                    if units > max_units:
                        issues.append(
                            ValidationIssue(
                                layer="PAYER_RULES",
                                type="MAX_UNITS_EXCEEDED",
                                severity=severity,
                                message=rule.get("message") or f"CPT {cpt} exceeds maximum units.",
                                fix=f"Reduce units to {max_units} or provide exception documentation.",
                                evidence=f"{units} units",
                                penalty=penalty,
                            )
                        )

        if rule_type == "CPT_NOT_ALLOWED_FOR_SPECIALTY":
            deterministic_rule_ids.append(rule.get("rule_id"))
            cpt = normalize_code(condition.get("cpt"))
            blocked_specialty = compact_token(condition.get("specialty"))
            if cpt in claim.cpt_codes and blocked_specialty == compact_token(claim.specialty):
                issues.append(
                    ValidationIssue(
                        layer="PAYER_RULES",
                        type="CPT_NOT_ALLOWED_FOR_SPECIALTY",
                        severity=severity,
                        message=rule.get("message") or f"CPT {cpt} is not allowed for specialty {claim.specialty}.",
                        fix="Route to coding review or correct the specialty/procedure code.",
                        evidence=f"Rule {rule.get('rule_id')}",
                        penalty=penalty,
                    )
                )

        if rule_type == "PAYER_RULES" and rule_condition_matches_claim(rule, claim):
            deterministic_rule_ids.append(rule.get("rule_id"))
            issues.append(
                ValidationIssue(
                    layer="PAYER_RULES",
                    type=normalize_code(rule.get("rule_id")) or "PAYER_RULE",
                    severity=severity,
                    message=rule.get("message") or "Payer rule requires review.",
                    fix=action.get("fix_template"),
                    evidence=f"Rule {rule.get('rule_id')}",
                    penalty=penalty,
                )
            )

    kg_enabled = bool(
        state.get("use_knowledge_graph", env_bool("USE_VALIDATION_KG", True))
    )
    if kg_enabled:
        kg = load_local_coding_kg(state.get("knowledge_graph_path"))
        kg_issues, kg_raw = kg_payer_rule_issues(claim=claim, kg=kg)
        issues.extend(kg_issues)
    else:
        kg_raw = {"enabled": False, "reason": "USE_VALIDATION_KG is false"}

    llm_issues, llm_raw = run_llm_payer_rule_checks(
        state=state,
        claim=claim,
        rules=rules,
    )
    issues.extend(llm_issues)

    result = CheckResult(
        layer="PAYER_RULES",
        passes=not any(issue.severity == "FAIL" for issue in issues),
        issues=issues,
        raw_data={
            "rules_applied": [rule.get("rule_id") for rule in rules],
            "deterministic_rule_ids": [
                rule_id for rule_id in deterministic_rule_ids if rule_id
            ],
            "knowledge_graph": kg_raw,
            "llm": llm_raw,
        },
    )
    return {**state, "payer_rules_result": result_dict(result)}


def check_timely_filing_node(state: ClaimValidationState) -> ClaimValidationState:
    if state.get("errors"):
        return state

    claim = ClaimInput.model_validate(state["claim"])
    submission_date = state.get("submission_date") or os.getenv("VALIDATION_SUBMISSION_DATE") or default_submission_date()
    rules = state.get("applicable_rules", [])
    issues: list[ValidationIssue] = []

    timely_rule = next((rule for rule in rules if rule.get("rule_type") == "TIMELY_FILING"), None)
    elapsed_days = days_between(claim.date_of_service, submission_date)
    if timely_rule:
        limit_days = int(timely_rule.get("action", {}).get("limit_days", 0))
        if elapsed_days > limit_days:
            issues.append(
                ValidationIssue(
                    layer="TIMELY_FILING",
                    type="FILING_DEADLINE_EXCEEDED",
                    severity=timely_rule.get("severity", "FAIL"),
                    message=(
                        f"Claim is {elapsed_days} days after service date, "
                        f"exceeding the {limit_days}-day filing limit."
                    ),
                    fix="Do not submit unless an exception or appeal path is available.",
                    evidence=f"Service date {claim.date_of_service}, submission date {submission_date}",
                    penalty=5,
                )
            )

    result = CheckResult(
        layer="TIMELY_FILING",
        passes=not any(issue.severity == "FAIL" for issue in issues),
        issues=issues,
        raw_data={"elapsed_days": elapsed_days, "submission_date": submission_date},
    )
    return {**state, "timely_filing_result": result_dict(result)}


def calculate_score_node(state: ClaimValidationState) -> ClaimValidationState:
    if state.get("errors"):
        return state

    results = {
        "FHIR_CONTEXT": state.get("fhir_context_result"),
        "ELIGIBILITY": state.get("eligibility_result"),
        "PRIOR_AUTHORIZATION": state.get("prior_auth_result"),
        "CODING": state.get("coding_result"),
        "DOCUMENTATION": state.get("documentation_result"),
        "PAYER_RULES": state.get("payer_rules_result"),
        "TIMELY_FILING": state.get("timely_filing_result"),
    }
    issues = [
        issue
        for result in results.values()
        if result
        for issue in result.get("issues", [])
    ]
    layer_caps = layer_caps_from_rules(state.get("applicable_rules") or state.get("payer_rules", []))
    layer_scoring: dict[str, dict[str, Any]] = {}

    for layer, result in results.items():
        layer_issues = result.get("issues", []) if result else []
        raw_deduction = round(
            sum(float(issue.get("penalty", 0.0)) for issue in layer_issues),
            2,
        )
        max_deduction = float(layer_caps.get(layer, 0.0))
        applied_deduction = round(min(raw_deduction, max_deduction), 2)
        layer_scoring[layer] = {
            "raw_deduction": raw_deduction,
            "max_deduction": max_deduction,
            "applied_deduction": applied_deduction,
            "capped": raw_deduction > max_deduction,
            "issue_count": len(layer_issues),
            "fail_count": len([issue for issue in layer_issues if issue.get("severity") == "FAIL"]),
            "warn_count": len([issue for issue in layer_issues if issue.get("severity") == "WARN"]),
        }

    total_raw_deduction = round(
        sum(layer["raw_deduction"] for layer in layer_scoring.values()),
        2,
    )
    total_applied_deduction = round(
        sum(layer["applied_deduction"] for layer in layer_scoring.values()),
        2,
    )
    score = max(0.0, round(100.0 - total_applied_deduction, 2))
    normalized_score = round(
        100.0
        * (
            1.0
            - (
                total_applied_deduction
                / max(sum(layer_caps.values()), 1.0)
            )
        ),
        2,
    )

    if score >= 85:
        routing: RoutingDecision = "READY_TO_SUBMIT"
    elif score >= 60:
        routing = "NEEDS_REVIEW"
    else:
        routing = "HOLD_CRITICAL"

    return {
        **state,
        "score": score,
        "scoring": {
            "method": "capped_subtractive_by_validation_layer",
            "score": score,
            "normalized_score": normalized_score,
            "total_raw_deduction": total_raw_deduction,
            "total_applied_deduction": total_applied_deduction,
            "layer_caps": layer_caps,
            "layers": layer_scoring,
        },
        "routing": routing,
        "final_issues": issues,
    }


def generate_report_node(state: ClaimValidationState) -> ClaimValidationState:
    if state.get("errors"):
        return state

    claim = ClaimInput.model_validate(state["claim"])
    final_issues = state.get("final_issues", [])
    needs_prior_authorization_agent = any(
        issue.get("type") == "MISSING_PRIOR_AUTH" for issue in final_issues
    )
    results = {
        "fhir_context": state.get("fhir_context_result"),
        "eligibility": state.get("eligibility_result"),
        "prior_authorization": state.get("prior_auth_result"),
        "coding": state.get("coding_result"),
        "documentation": state.get("documentation_result"),
        "payer_rules": state.get("payer_rules_result"),
        "timely_filing": state.get("timely_filing_result"),
    }
    report = {
        "claim_id": claim.claim_id,
        "payer_id": claim.payer_id,
        "plan_id": claim.plan_id,
        "patient_id": claim.patient_id,
        "date_of_service": claim.date_of_service,
        "score": state.get("score", 0.0),
        "scoring": state.get("scoring", {}),
        "routing": state.get("routing"),
        "summary": {
            "total_issues": len(final_issues),
            "critical_issues": len([issue for issue in final_issues if issue.get("severity") == "FAIL"]),
            "warnings": len([issue for issue in final_issues if issue.get("severity") == "WARN"]),
            "information": len([issue for issue in final_issues if issue.get("severity") == "INFO"]),
        },
        "checks": {
            name: {
                "passes": result.get("passes"),
                "issue_count": len(result.get("issues", [])),
            }
            for name, result in results.items()
            if result
        },
        "issues": final_issues,
        "recommended_next_step": {
            "READY_TO_SUBMIT": "Send to submission queue.",
            "NEEDS_REVIEW": "Route to RCM review before submission.",
            "HOLD_CRITICAL": "Block submission until critical issues are fixed.",
        }.get(state.get("routing"), "Review validation output."),
        "recommended_next_agent": (
            "PriorAuthorizationAgent"
            if needs_prior_authorization_agent
            else "ClaimSubmissionAgent"
            if state.get("routing") == "READY_TO_SUBMIT"
            else "HumanReview"
        ),
        "approval": state.get("approval", {}),
        "warnings": state.get("warnings", []),
        "validation_tools": state.get("validation_tools") or validation_tool_registry(),
        "validation_events": state.get("validation_events", []),
        "generated_at": utc_now(),
    }
    return {
        **state,
        "report": report,
        "status": "CLAIM_VALIDATION_REPORTED",
    }


def approval_gate_node(state: ClaimValidationState) -> ClaimValidationState:
    if state.get("errors"):
        return state

    routing = state.get("routing")
    existing_approval = state.get("approval") or {}
    review_required = bool(
        existing_approval.get("required")
        or routing in {"NEEDS_REVIEW", "HOLD_CRITICAL"}
        or state.get("interrupt_before_approval")
    )
    decision = existing_approval.get("decision")

    if state.get("interrupt_before_approval") and not decision:
        approval = {
            **existing_approval,
            "required": True,
            "status": "pending",
            "reason": "Human review is required before this claim can move to submission.",
            "requested_at": existing_approval.get("requested_at") or utc_now(),
        }
        report = {
            **state.get("report", {}),
            "approval": approval,
            "validation_events": state.get("validation_events", []),
        }
        return {
            **state,
            "approval": approval,
            "report": report,
            "status": "CLAIM_VALIDATION_AWAITING_APPROVAL",
            "messages": add_message(
                state,
                "assistant",
                "Validation is complete and waiting for human approval.",
                {"routing": routing},
            ),
        }

    approval_status = existing_approval.get("status")
    if not approval_status:
        approval_status = "approved" if decision == "approved" else "pending" if review_required else "not_required"

    approval = {
        **existing_approval,
        "required": review_required,
        "status": approval_status,
        "routing": routing,
        "decided_at": existing_approval.get("decided_at") or (utc_now() if decision else None),
    }
    report = {
        **state.get("report", {}),
        "approval": approval,
        "validation_events": state.get("validation_events", []),
    }
    return {
        **state,
        "approval": approval,
        "report": report,
        "status": "CLAIM_VALIDATION_COMPLETED",
    }


