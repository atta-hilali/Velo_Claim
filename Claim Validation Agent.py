class FHIRClient:
    def __init__(
        self,
        base_url: str,
        token_manager: FHIRTokenManager | None = None,
        access_token: str | None = None,
    ) -> None:
        self.base_url = base_url.rstrip("/")
        self.token_manager = token_manager
        self.access_token = access_token

    @property
    def is_configured(self) -> bool:
        return bool(self.base_url)

    def _request_json(self, path: str, params: dict[str, str] | None = None) -> dict[str, Any]:
        query = f"?{urllib.parse.urlencode(params or {})}" if params else ""
        url = f"{self.base_url}/{path.lstrip('/')}{query}"

        for attempt in range(2):
            headers = {"Accept": "application/fhir+json, application/json"}
            token = self.access_token
            if self.token_manager:
                token = self.token_manager.access_token()
            if token:
                headers["Authorization"] = f"Bearer {token}"

            request = urllib.request.Request(url, headers=headers, method="GET")
            try:
                with urllib.request.urlopen(request, timeout=30) as response:
                    return json.loads(response.read().decode("utf-8"))
            except urllib.error.HTTPError as exc:
                body = exc.read().decode("utf-8", errors="replace")
                if exc.code == 401 and attempt == 0 and self.token_manager:
                    self.token_manager.invalidate()
                    continue
                raise FHIRRequestError(
                    f"FHIR request failed {exc.code} for {url}: {body}",
                    status_code=exc.code,
                    url=url,
                ) from exc

        raise FHIRRequestError(
            f"FHIR request failed after token refresh for {url}",
            status_code=401,
            url=url,
        )

    def read(self, resource_type: str, resource_id: str | None) -> dict[str, Any] | None:
        if not resource_id:
            return None
        return self._request_json(f"{resource_type}/{reference_id(resource_id)}")

    def search(self, resource_type: str, params: dict[str, str]) -> list[dict[str, Any]]:
        bundle = self._request_json(resource_type, params)
        return [
            entry["resource"]
            for entry in bundle.get("entry", [])
            if isinstance(entry.get("resource"), dict)
            and entry["resource"].get("resourceType") == resource_type
        ]


def get_fhir_client() -> Any | None:
    adapter_name = normalize_fhir_adapter_name(os.getenv("FHIR_ADAPTER") or "generic")
    if adapter_name != "generic":
        adapter = get_shared_fhir_adapter()
        if adapter:
            return adapter

    base_url = os.getenv("FHIR_BASE_URL")
    if not base_url:
        return None
    return FHIRClient(base_url=base_url, token_manager=fhir_token_manager)


def claim_from_state(state: ClaimValidationState) -> dict[str, Any]:
    raw = state.get("raw_claim") or {}
    if state.get("claim") and "claim_id" in state["claim"]:
        return state["claim"]
    if raw.get("claim"):
        return raw["claim"]
    if raw.get("claim_draft"):
        return raw["claim_draft"]
    if raw.get("full_result", {}).get("claim"):
        return raw["full_result"]["claim"]
    return raw


def claim_input_from_canonical(claim: dict[str, Any]) -> ClaimInput:
    if "date_of_service" in claim:
        return ClaimInput.model_validate(claim)

    patient = claim.get("patient", {})
    payer = claim.get("payer", {})
    provider = claim.get("provider", {})
    source_coverage = claim.get("source_resources", {}).get("coverage", {})
    amount = claim.get("amount", {})
    clinical_context = claim.get("clinical_context", {})
    procedures = as_list(claim.get("procedures"))
    diagnoses = as_list(claim.get("diagnoses"))
    line_items = as_list(claim.get("line_items"))

    procedure_codes = [
        normalize_code(item.get("code"))
        for item in procedures
        if item.get("code")
    ]
    if not procedure_codes:
        procedure_codes = [
            normalize_code(item.get("code"))
            for item in line_items
            if item.get("code")
        ]

    cpt_codes = [
        normalize_code(item.get("code"))
        for item in procedures
        if item.get("code") and normalize_code(item.get("system")) in {"CPT", "HCPCS", "CDT"}
    ]
    if not cpt_codes:
        cpt_codes = procedure_codes

    return ClaimInput(
        claim_id=claim.get("claim_id") or claim.get("id") or "UNKNOWN_CLAIM",
        patient_id=patient.get("id") or reference_id(claim.get("patient_id")) or "UNKNOWN_PATIENT",
        payer_id=payer.get("id") or payer.get("name") or claim.get("payer_id") or "UNKNOWN_PAYER",
        plan_id=(
            payer.get("plan_id")
            or payer.get("plan")
            or claim.get("plan_id")
            or first_coverage_plan_id(source_coverage)
        ),
        provider_id=provider.get("id") or claim.get("provider_id"),
        organization_id=provider.get("facility_id") or claim.get("organization_id"),
        encounter_id=claim.get("encounter_id"),
        coverage_id=payer.get("coverage_id") or claim.get("coverage_id"),
        date_of_service=claim.get("service_date") or claim.get("date_of_service") or utc_now().split("T")[0],
        specialty=provider.get("specialty") or claim.get("specialty"),
        icd_codes=[
            normalize_code(item.get("code"))
            for item in diagnoses
            if item.get("code")
        ],
        cpt_codes=cpt_codes,
        procedure_codes=procedure_codes,
        total_amount=amount.get("net") or amount.get("billed") or claim.get("total_amount"),
        currency=amount.get("currency") or claim.get("currency"),
        soap_note_excerpt=clinical_context.get("soap_note_excerpt") or claim.get("soap_note_excerpt"),
        attachments=as_list(claim.get("attachments")),
        line_items=line_items,
        prior_auth=claim.get("prior_auth"),
    )


