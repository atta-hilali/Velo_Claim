def normalize_token(value: Any) -> str:
    return str(value or "").strip().lower()


def normalize_code(value: Any) -> str:
    return str(value or "").strip().upper()


def compact_token(value: Any) -> str:
    return "".join(char for char in normalize_token(value) if char.isalnum())


def payer_match_tokens(value: Any) -> set[str]:
    token = compact_token(value)
    tokens = {normalize_token(value), token}
    aliases = {
        "tawuniya": {"tawuniya", "tawuniyasa", "twn"},
        "tawuniyasa": {"tawuniya", "tawuniyasa", "twn"},
        "twn": {"tawuniya", "tawuniyasa", "twn"},
        "daman": {"daman", "damanuae", "damanabudhabi", "daman_ae"},
        "damanae": {"daman", "damanuae", "damanabudhabi", "daman_ae"},
        "damanuae": {"daman", "damanuae", "damanabudhabi", "daman_ae"},
        "default": {"default", "*"},
    }
    tokens.update(aliases.get(token, set()))
    tokens.update(payer_alias_tokens(value))
    return {item for item in tokens if item}


def payer_matches(rule_payer_id: Any, claim_payer_id: Any) -> bool:
    rule_token = normalize_token(rule_payer_id)
    if rule_token in {"*", "default"}:
        return True
    return bool(payer_match_tokens(rule_payer_id) & payer_match_tokens(claim_payer_id))


def plan_match_tokens(value: Any) -> set[str]:
    raw_values = [
        value,
        *as_list(value.get("aliases") if isinstance(value, dict) else None),
    ]
    tokens = {
        compact_token(item)
        for item in raw_values
        if compact_token(item)
    }
    return tokens


def plan_matches(rule_plan_id: Any, claim_plan_id: Any) -> bool:
    if not rule_plan_id:
        return True
    rule_token = normalize_token(rule_plan_id)
    if rule_token in {"*", "default"}:
        return True
    return bool(plan_match_tokens(rule_plan_id) & plan_match_tokens(claim_plan_id))


def first_coverage_plan_id(coverage: dict[str, Any] | None) -> str | None:
    if not coverage:
        return None
    for key in ("plan_id", "planId", "plan", "network", "product"):
        value = coverage.get(key)
        if value:
            return str(value)

    for class_item in as_list(coverage.get("class")):
        if not isinstance(class_item, dict):
            continue
        class_type = first_codeable_text(class_item.get("type")) or class_item.get("type")
        if compact_token(class_type) in {"plan", "product", "network", "policy"}:
            return str(class_item.get("value") or class_item.get("name") or "")
    return None


def parse_iso_date(value: str | None) -> date | None:
    if not value:
        return None
    try:
        return datetime.fromisoformat(str(value).replace("Z", "+00:00")).date()
    except ValueError:
        try:
            return date.fromisoformat(str(value).split("T")[0])
        except ValueError:
            return None


def date_in_period(service_date: str, period: dict[str, Any]) -> bool:
    current = parse_iso_date(service_date)
    if not current:
        return True
    start = parse_iso_date(period.get("start"))
    end = parse_iso_date(period.get("end"))
    if start and current < start:
        return False
    if end and current > end:
        return False
    return True


def days_between(start: str, end: str) -> int:
    start_date = parse_iso_date(start)
    end_date = parse_iso_date(end)
    if not start_date or not end_date:
        return 0
    return (end_date - start_date).days


def compact_json(value: Any, max_chars: int = 12000) -> str:
    text = json.dumps(value, ensure_ascii=True, indent=2, default=str)
    return text[:max_chars]


def extract_json_object(text: str) -> dict[str, Any]:
    try:
        return json.loads(text)
    except json.JSONDecodeError:
        pass

    match = re.search(r"\{.*\}", text, flags=re.DOTALL)
    if not match:
        raise ValueError("LLM response did not contain a JSON object.")
    return json.loads(match.group(0))


def build_json_headers(
    *,
    bearer_token: str | None = None,
    accept: str = "application/json",
) -> dict[str, str]:
    headers = {"Accept": accept, "Content-Type": "application/json"}
    if bearer_token:
        headers["Authorization"] = f"Bearer {bearer_token}"
    return headers


def post_json(
    *,
    base_url: str,
    path: str,
    payload: dict[str, Any],
    bearer_token: str | None = None,
    timeout_seconds: int = 60,
) -> dict[str, Any]:
    url = f"{base_url.rstrip('/')}/{path.lstrip('/')}"
    request = urllib.request.Request(
        url,
        data=json.dumps(payload, ensure_ascii=True).encode("utf-8"),
        headers=build_json_headers(bearer_token=bearer_token),
        method="POST",
    )
    try:
        with urllib.request.urlopen(request, timeout=timeout_seconds) as response:
            return json.loads(response.read().decode("utf-8"))
    except urllib.error.HTTPError as exc:
        body = exc.read().decode("utf-8", errors="replace")
        raise RuntimeError(f"POST {url} failed with HTTP {exc.code}: {body}") from exc


