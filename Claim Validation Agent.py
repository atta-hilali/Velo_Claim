"""
Claim Validation Agent for Velo Claim.

This is the second agent in the Velo Claim chain. It receives a prepared draft
claim, enriches it with available FHIR context, runs deterministic validation
checks, calculates a score, routes the claim, and produces an RCM-readable
validation report.

Design:
    - LangGraph controls the validation workflow.
    - Eligibility, prior authorization, documentation, timely filing, scoring,
      and routing are deterministic.
    - Payer-rule validation combines deterministic rules, optional local KG
      code-pair checks, and optional LLM review for rules marked requires_llm.
    - The agent can accept either a compact ClaimInput or the canonical claim
      generated by Claim Preparation Agent.
"""

